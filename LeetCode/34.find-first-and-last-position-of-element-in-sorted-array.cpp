// @lc code=start
class Solution {
public:
    vector<int> searchRange(vector<int>& nums, int target) {
        int l = 0 , r = nums.size() - 1;
        while (l < r) {
            int mid = l + r >> 1;
            if (nums[mid] >= target) r = mid;
            else l = mid + 1;
        }
        if (l > r || nums[l] != target) return {-1, -1};
        while (r + 1 < nums.size() && nums[r + 1] == nums[l]) ++r;
        return {l, r};
    }
};
